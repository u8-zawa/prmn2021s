package lecture01;

public class Exercise03 {
    public static void main(String[] args) {
        int age = 19;

        if (age < 20) {
            // age が20歳未満のとき
            System.out.println("I am " + age + " years old so I cannot drink liquor.");
        }else {
            // age が20歳以上のとき
            System.out.println("I am " + age + " years old so I can drink liquor.");
        }
    }
}
